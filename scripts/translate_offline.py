def digit_to_italian(n: int) -> str:
    """Convert an integer to Italian words."""
    if n in DIGIT_WORDS_IT:
        return DIGIT_WORDS_IT[n]
    return str(n)


def translate_number_entry(subtitle: str) -> str:
    """Traduce una voce numerica."""
    text = subtitle.strip().lower()

    # Direct number word
    if text in NUMBER_WORDS:
        return NUMBER_WORDS[text]

    # Pattern: "N-point M-seconds" (e.g. "1-point 3-seconds")
    m = re.match(r"(\d+)-point (\d+)-seconds?$", text)
    if m:
        left = digit_to_italian(int(m.group(1)))
        right = digit_to_italian(int(m.group(2)))
        return f"{left} punto {right} secondi"

    # Pattern: "N-point M" (e.g. "10-point 5")
    m = re.match(r"(\d+)-point (\d+)$", text)
    if m:
        left = digit_to_italian(int(m.group(1)))
        right = digit_to_italian(int(m.group(2)))
        return f"{left} punto {right}"

    # Pattern: "0-point N" (e.g. "0-point 5")
    m = re.match(r"0-point (\d+)$", text)
    if m:
        right = digit_to_italian(int(m.group(1)))
        return f"zero punto {right}"

    # Compound: "X point Y" (word form)
    m = re.match(r"(.+?) point (.+)", text)
    if m:
        left = translate_number_entry(m.group(1))
        right = translate_number_entry(m.group(2))
        return f"{left} punto {right}"

    # "X and a half"
    m = re.match(r"(.+?) and a half", text)
    if m:
        left = translate_number_entry(m.group(1))
        return f"{left} e mezzo"

    # Pattern: "M N" — lap time format (e.g. "1 32" = 1 min 32 sec)
    m = re.match(r"^(\d+) (\d+)$", text)
    if m:
        mins = digit_to_italian(int(m.group(1)))
        secs = digit_to_italian(int(m.group(2)))
        return f"{mins} {secs}"

    # Pure digit string
    m = re.match(r"^(\d+)$", text)
    if m:
        n = int(m.group(1))
        return digit_to_italian(n)

    # Compound with spaces: try word by word
    words = text.split()
    if len(words) > 1:
        return " ".join(NUMBER_WORDS.get(w, w) for w in words)

    # Numeric digits — keep as-is in subtitle
    return text


