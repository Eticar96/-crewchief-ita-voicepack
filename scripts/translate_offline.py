def translate_codriver(subtitle: str) -> str:
    """Traduce una nota pace codriver dall'inglese all'italiano."""
    text = subtitle.lower().strip()

    # Try exact match in CODRIVER_TRANSLATIONS first (handles multi-word phrases)
    if text in CODRIVER_TRANSLATIONS:
        return CODRIVER_TRANSLATIONS[text]

    # Try with original case
    if subtitle.strip() in CODRIVER_TRANSLATIONS:
        return CODRIVER_TRANSLATIONS[subtitle.strip()]

    # Pattern: "into N left/right"
    m = re.match(r"into (\d) (left|right)", text)
    if m:
        num_it = ["", "uno", "due", "tre", "quattro", "cinque", "sei"][int(m.group(1))]
        dir_it = "sinistra" if m.group(2) == "left" else "destra"
        return f"in {num_it} {dir_it}"

    # Pattern: "into <descriptive> left/right"
    m = re.match(r"into (\w+) (left|right)", text)
    if m:
        desc = m.group(1)
        dir_it = "sinistra" if m.group(2) == "left" else "destra"
        desc_it = CODRIVER_TRANSLATIONS.get(desc, desc)
        return f"in {desc_it} {dir_it}"

    # Pattern: "N left/right" standalone
    m = re.match(r"^(\d) (left|right)$", text)
    if m:
        num_it = ["", "uno", "due", "tre", "quattro", "cinque", "sei"][int(m.group(1))]
        dir_it = "sinistra" if m.group(2) == "left" else "destra"
        return f"{num_it} {dir_it}"

    # Pattern: "<descriptive> left/right" standalone
    m = re.match(r"^(\w+) (left|right)$", text)
    if m:
        desc = m.group(1)
        dir_it = "sinistra" if m.group(2) == "left" else "destra"
        desc_it = CODRIVER_TRANSLATIONS.get(desc, desc)
        return f"{desc_it} {dir_it}"

    # General word-by-word for remaining codriver entries
    words = text.split()
    translated = []
    for w in words:
        if w in CODRIVER_TRANSLATIONS:
            translated.append(CODRIVER_TRANSLATIONS[w])
        elif w.isdigit():
            nums = ["zero", "uno", "due", "tre", "quattro", "cinque", "sei",
                    "sette", "otto", "nove", "dieci"]
            idx = int(w)
            translated.append(nums[idx] if idx < len(nums) else w)
        else:
            translated.append(w)
    return " ".join(translated)


