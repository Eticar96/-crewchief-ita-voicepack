def translate_line(audio_path: str, audio_filename: str, subtitle: str, tts_text: str) -> tuple[str, str]:
    """Traduce una singola riga del CSV.

    Restituisce (subtitle_ita, tts_ita).
    """
    path_lower = audio_path.lower()

    # 0. Try exact match in manual translations FIRST (works for all categories)
    sub_stripped = subtitle.strip()
    if sub_stripped in MANUAL_TRANSLATIONS:
        it = MANUAL_TRANSLATIONS[sub_stripped]
        return it, it
    # Case-insensitive fallback
    sub_lower = sub_stripped.lower()
    for en, it in MANUAL_TRANSLATIONS.items():
        if en.lower() == sub_lower:
            return it, it

    # 1. Codriver pace notes
    if "\\codriver\\" in path_lower:
        it = translate_codriver(subtitle)
        return it, it

    # 2. Corner names
    if "\\corners\\" in path_lower:
        it = translate_corner_name(subtitle)
        return it, it

    # 3. Numbers
    if "\\numbers\\" in path_lower:
        it = translate_number_entry(subtitle)
        return it, it

    # 4. All other phrases — try generic patterns
    sub_it, tts_it = translate_generic(subtitle, tts_text, audio_path)
    return sub_it, tts_it


